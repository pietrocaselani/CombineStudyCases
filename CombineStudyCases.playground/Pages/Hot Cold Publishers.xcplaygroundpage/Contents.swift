 # Hot/Cold Publishers

 Hot publishers use resources (produce heat) no matter if there is any listeners.

 Cold publishers don't use resources (don't produce heat) until a listener subscribes.

 Hot publishers are variables, UI events, current time, socket connection

 Cold publishers are async operation, HTTP, IO

 Hot publishers usually are stateful

 Cold publishers usually are stateless

